def main():
    # Print integers from 0 to 10
    print("\n")
    for i in range(11):
        print(i)

    # Print integers from 5 to 10
    print("\n")
    for i in range(5, 11):
        print(i)

    # Print even integers from 0 to 10
    print("\n")
    for i in range(0, 11, 2):
        print(i)
        
main()